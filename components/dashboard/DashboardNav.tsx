'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'

const nav = [
  { href: '/dashboard', label: 'Overview', icon: ChartIcon },
  { href: '/dashboard/pos', label: 'POS', icon: PosIcon },
  { href: '/dashboard/schedule', label: 'Schedule', icon: CalendarIcon },
  { href: '/dashboard/availability', label: 'Availability', icon: ClockIcon },
  { href: '/dashboard/time-off', label: 'Time off', icon: PalmIcon },
  { href: '/dashboard/no-shows', label: 'No-shows', icon: AlertIcon },
  { href: '/dashboard/settings/services', label: 'Services', icon: ScissorsIcon },
  { href: '/dashboard/settings/barbers', label: 'Barbers', icon: UsersIcon },
]

export function DashboardNav() {
  const pathname = usePathname()

  return (
    <ul className="space-y-1">
      {nav.map(({ href, label, icon: Icon }) => {
        const isActive = pathname === href || (href !== '/dashboard' && pathname.startsWith(href))
        return (
          <li key={href}>
            <Link
              href={href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition ${
                isActive
                  ? 'bg-headz-red text-white'
                  : 'text-white/70 hover:text-white hover:bg-white/5'
              }`}
            >
              <Icon className="w-5 h-5 shrink-0" />
              {label}
            </Link>
          </li>
        )
      })}
    </ul>
  )
}

